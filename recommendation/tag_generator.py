def generate_tags():
    cfg = get_mysql_cfg()
    conn = pymysql.connect(**cfg, autocommit=True)
    cur = conn.cursor()

    # 获取所有图书及作者
    cur.execute("""
        SELECT b.book_id, b.title, b.description, b.score,
               GROUP_CONCAT(DISTINCT a.name SEPARATOR '||') AS authors
        FROM book b
        LEFT JOIN book_author ba ON b.book_id=ba.book_id
        LEFT JOIN author a ON ba.author_id=a.author_id
        GROUP BY b.book_id
    """)
    books = cur.fetchall()

    cur.execute("DELETE FROM book_tag")
    cur.execute("DELETE FROM tag")

    all_tags, stats = {}, Counter()

    for book_id, title, desc, score, authors_str in books:
        tags = set()
        author_names = authors_str.split('||') if authors_str else []

        # 1. 作者知识库匹配
        for aname in author_names:
            for KnownAuthor, (nationality, genres) in AUTHOR_GENRE.items():
                if KnownAuthor in aname:
                    tags.add(nationality + '文学')
                    for g in genres:
                        tags.add(g)

        # 2. 书名精确匹配
        for tname, ttags in BOOK_GENRE.items():
            if tname in title:
                for g in ttags:
                    tags.add(g)

        # 3. 无标签兜底
        if not tags:
            if score and score >= 9.0:
                tags.add('经典名著')
            elif score and score >= 8.0:
                tags.add('高分佳作')
            else:
                tags.add('文学')

        # 写入
        for tname in tags:
            if tname not in all_tags:
                cur.execute("INSERT INTO tag (name) VALUES (%s) ON DUPLICATE KEY UPDATE tag_id=LAST_INSERT_ID(tag_id)", (tname,))
                tid = cur.lastrowid
                if not tid: cur.execute("SELECT tag_id FROM tag WHERE name=%s", (tname,)); tid = cur.fetchone()[0]
                all_tags[tname] = tid
            else:
                tid = all_tags[tname]
            cur.execute("INSERT IGNORE INTO book_tag (book_id, tag_id) VALUES (%s,%s)", (book_id, tid))
            stats[tname] += 1

    cur.close(); conn.close()
    print(f"  生成 {len(all_tags)} 个标签，覆盖 {len(books)} 本书")
    for t, n in stats.most_common(40):
        print(f"    {t}: {n}")
    return len(all_tags), stats


if __name__ == '__main__':
    generate_tags()
